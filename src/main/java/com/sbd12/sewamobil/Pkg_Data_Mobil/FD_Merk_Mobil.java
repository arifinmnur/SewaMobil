    public void tampilData() throws ClassNotFoundException {

        tableMerkMobil = new MerkMobilTableModel();
        tableMerkMobil.setData(merkMobils);
        jTable1.setModel(tableMerkMobil);
        jTable1.setSelectionModel(new ForcedListSelectionModel());

    }

    public void refreshData() throws ClassNotFoundException {
        merkMobils = db.listSemua();
        tableMerkMobil.setData(merkMobils);
        tableMerkMobil.fireTableDataChanged();
        jTable1.changeSelection(0, 0, false, false);
    }

