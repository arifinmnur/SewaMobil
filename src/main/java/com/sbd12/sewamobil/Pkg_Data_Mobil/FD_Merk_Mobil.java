    private void jb_pilihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_pilihActionPerformed
        // TODO add your handling code here:
         int baris = jTable1.getSelectedRow();
        String id_mobil = (String) tableMerkMobil.getValueAt(baris, 0);
        JenisMobil jenisMobil = new JenisMobil();
        merkMobil=db.pilih_data(id_mobil);
        jenisMobil=db2.pilih_data(merkMobil.getId_jenis());
        String nama_merk = merkMobil.getNama_Merk_Mobil();
        String nama_prod = merkMobil.getNama_produsen_mobil();
        String nama_jenis = merkMobil.getNama_jenis();
        double hargaPerhhari = jenisMobil.getHarga_mobil();
        
        setId_merk(id_mobil);
        setNama_merk(nama_merk);
        setNama_prod(nama_prod);
        setNama_merk(nama_merk);
        setNama_jenis(nama_jenis);
        setHargaPerhhari(hargaPerhhari);
        
        setVisible(false);

    }//GEN-LAST:event_jb_pilihActionPerformed

